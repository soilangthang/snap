// Function to update blog content based on language
function updateBlogLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    
    const translations = blogTranslations[lang] || blogTranslations['en'];
    
    // Update common elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[key]) {
            if (key.includes('Content')) {
                el.innerHTML = translations[key];
            } else {
                el.textContent = translations[key];
            }
        }
    });
    
    // Update article content
    [1, 2, 3].forEach(num => {
        const articleEl = document.querySelector(`[data-article="${num}"]`);
        if (articleEl) {
            const titleEl = articleEl.querySelector('h2[data-i18n]');
            const contentEl = articleEl.querySelector('.content[data-i18n]');
            
            if (titleEl && translations[`article${num}Title`]) {
                titleEl.textContent = translations[`article${num}Title`];
            }
            
            if (contentEl && translations[`article${num}Content`]) {
                contentEl.innerHTML = translations[`article${num}Content`];
            }
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update language button
    const langCodes = { en: 'EN', hi: 'HI', vi: 'VI', id: 'ID' };
    const langBtn = document.getElementById('currentLang');
    if (langBtn) {
        langBtn.textContent = langCodes[lang] || 'EN';
    }
}

// Initialize language switcher for blog
function initBlogLanguageSwitcher() {
    const langBtn = document.getElementById('langBtn');
    const langSwitcher = document.querySelector('.lang-switcher');
    const langOptions = document.querySelectorAll('.lang-option');
    
    if (!langBtn || !langSwitcher) return;
    
    // Set initial language
    updateBlogLanguage(currentLang);
    
    // Toggle dropdown
    langBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        langSwitcher.classList.toggle('active');
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (!langSwitcher.contains(e.target)) {
            langSwitcher.classList.remove('active');
        }
    });
    
    // Handle language selection
    langOptions.forEach(option => {
        option.addEventListener('click', () => {
            const lang = option.getAttribute('data-lang');
            updateBlogLanguage(lang);
            langSwitcher.classList.remove('active');
        });
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    initBlogLanguageSwitcher();
});

